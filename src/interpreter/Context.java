package interpreter;

/**
 * The Class Conext.
 */
public class Context {

	/** The num 1. */
	private int num1;

	/** The num 2. */
	private int num2;

	/**
	 * Instantiates a new conext.
	 *
	 * @param num1
	 *            the num 1
	 * @param num2
	 *            the num 2
	 */
	public Context(int num1, int num2) {
		super();
		this.num1 = num1;
		this.num2 = num2;
	}

	/**
	 * Gets the num 1.
	 *
	 * @return the num 1
	 */
	public int getNum1() {
		return num1;
	}

	/**
	 * Gets the num 2.
	 *
	 * @return the num 2
	 */
	public int getNum2() {
		return num2;
	}

}
